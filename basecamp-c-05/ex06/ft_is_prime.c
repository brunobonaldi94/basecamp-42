int	ft_is_prime(int nb)
{
	int	i;

	if (nb == 2 || nb == 3)
		return (1);
	else if (nb < 2 || (nb % 2 == 0) || (nb % 3 == 0))
		return (0);
	i = 5;
	while (i < nb)
	{
		if ((nb % i) == 0)
			return (0);
		i++;
	}
	return (1);
}
