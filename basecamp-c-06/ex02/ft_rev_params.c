#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	int		j;
	char	new_line;

	i = argc - 1;
	j = 0;
	new_line = '\n';
	while (i > 0)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		i--;
		write(1, &new_line, 1);
	}
	return (0);
}
